    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int answer = JOptionPane.showConfirmDialog(null, "Â¿Desea cargar el Archivo?\nSi carga el archivo perdera los eventos no archivados","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(answer == JOptionPane.YES_OPTION){
        try {
            archiveclass.readFile(Pathselectedfile);
        } catch (FileNotFoundException | ParseException ex) {
            JOptionPane.showMessageDialog(this, "Archivo Erroneo","Error",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(CargarEvento.class.getName()).log(Level.SEVERE, null, ex);
            this.dispose();
        }
        finally{
        JOptionPane.showMessageDialog(this, "Se han cargado los elementos Seleccionados","Exito",JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        }
        }
        else{
        JOptionPane.showMessageDialog(this, "Operacion Cancelada","OK",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
        String command = evt.getActionCommand();
        if(command.equals(JFileChooser.APPROVE_SELECTION)){

            JOptionPane.showMessageDialog(this, "Archivo Seleccionado","Exito",JOptionPane.INFORMATION_MESSAGE);
            Pathselectedfile = jFileChooser1.getSelectedFile().getAbsolutePath();
        }
        else if(command.equals(JFileChooser.ERROR_OPTION)){
            JOptionPane.showMessageDialog(this, "Archivo Incorrecto","Error",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jFileChooser1ActionPerformed

